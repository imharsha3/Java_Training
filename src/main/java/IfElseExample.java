public class IfElseExample {
    public static void main(String[] args) {
        boolean a = false;
        boolean b = true;

        if (a) {
            System.out.println("a is true");
        } else {
            System.out.println("a is false");
        }

        if (b) {
            System.out.println("b is true");
        } else {
            System.out.println("b is false");
        }
    }
}
