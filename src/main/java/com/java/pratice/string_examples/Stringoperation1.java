package com.java.pratice.string_examples;

public class Stringoperation1 {
    public static void main(String ar[])
    {
        String s="JavaIsProgrammingLang";
        System.out.println(s.toUpperCase());//JAVA
        System.out.println(s.toLowerCase());//java
        System.out.println(s.startsWith("Ja"));
        System.out.println(s.endsWith("ng"));//Java(no change in original)
    }
}
