package com.java.pratice.string_examples;

public class TestStringConcatenation {
    public static void main(String[] args)
    {
        // str 1
        String str1 = "Hello";

        // + operator do concat here!
        String result = str1 + ", World!";

        // Output: Hello, World!
        System.out.println(result);
    }
}
